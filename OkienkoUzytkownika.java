    private void ZapiszButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ZapiszButtonActionPerformed
                
        Uzytkownik uzyt = new Uzytkownik();
               
        String IDStr =jIDField.getText();
        int Id = 0;
        
        if( !IDStr.isEmpty())
        {
            Id = Integer.parseInt(IDStr);
            uzyt.setIDUzytkownika(Id);
        }
               
        uzyt.setImieUzytkownika(jImieField.getText());
        uzyt.setNazwiskoUzytkownika(jNazwiskoField.getText());
        uzyt.setLoginUzytkownika(jLogin.getText());
        uzyt.setHasloUzytkownika(jHaslo.getText());
        uzyt.setAdministrator(jAdministrator.isSelected());
        
        
        if( uzyt.sprawdzDane()) {
        
            AppSession appses = new AppSession();
            Session session= appses.OpenSession();
        
        
            Transaction trans = session.beginTransaction();     
            if ( Id==0)
                session.save(uzyt);  
            else
                session.update(uzyt);
            trans.commit();   
        
            appses.CloseSession();

            setVisible(false);
            ListaUzyt.wczytaj();
        }       
    }//GEN-LAST:event_ZapiszButtonActionPerformed

