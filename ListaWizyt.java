    private void EdytujButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EdytujButtonActionPerformed

        int Selected = jTable1.getSelectedRow();
        if(Selected >= 0 )
        {   PozycjaDoUstawienia = Selected;
            String IDSelected = jTable1.getModel().getValueAt(Selected, 0).toString();
            
            AppSession appses = new AppSession();
            Session session= appses.OpenSession();


            Wizyta Wiz;

            Wiz = (Wizyta) session.createSQLQuery("select * from wizyta where ID_Wizyty="+IDSelected)
            .addEntity(Wizyta.class).uniqueResult();
            Wiz.setPacjent(jTable1.getModel().getValueAt(Selected, 2).toString());
            Wiz.setStomatolog(jTable1.getModel().getValueAt(Selected, 3).toString());

            OkienkoWizyty ds = new OkienkoWizyty();
            ds.UstawWizyte(Wiz);
            ds.UstawListe(this);

            appses.CloseSession();
 
             ds.setVisible(true);

        }
        else
            JOptionPane.showConfirmDialog(null, "Wybierz wizytę", "Wskazówka", JOptionPane.DEFAULT_OPTION);
    }//GEN-LAST:event_EdytujButtonActionPerformed

    private void UsunButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UsunButtonActionPerformed
        int Selected = jTable1.getSelectedRow();

        if(Selected >= 0 )
        {   
            int dialogButton= JOptionPane.showConfirmDialog (null, "Czy napewno chcesz usunąć?","Potwierdź",JOptionPane.YES_NO_OPTION);

            if (dialogButton == JOptionPane.YES_OPTION) {
                String IDSelected = jTable1.getModel().getValueAt(Selected, 0).toString();
        
                AppSession appses = new AppSession();
                Session session= appses.OpenSession();
                Transaction trans = session.beginTransaction();

                Wizyta Wiz = new Wizyta();
                Wiz.setIDWizyty(Integer.parseInt(IDSelected));

                session.delete(Wiz);

                trans.commit();
                appses.CloseSession();

                DefaultTableModel tblModel  = (DefaultTableModel)jTable1.getModel();
                tblModel.removeRow(Selected);
            }

        }
        else
            JOptionPane.showConfirmDialog(null, "Wybierz wizytę", "Wskazówka", JOptionPane.DEFAULT_OPTION);
    }//GEN-LAST:event_UsunButtonActionPerformed

