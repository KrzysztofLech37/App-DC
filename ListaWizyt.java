    public int wczytaj()
    {
        AppSession appses = new AppSession();
        Session session= appses.OpenSession();
              
        String SqlStr;
        String SqlCol;
        String Nazwisko = jNazwiskoField.getText();
        
        SqlCol = "select ID_wizyty, Data_wizyty, p.nazwisko as nazwisko_pat, p.imie as imie_pat, s.nazwisko, s.imie, zab, kod_diagnozy, kod_uslugi from Wizyta w, Pacjent p, Stomatolog s where p.ID_pacjenta =w.ID_pacjenta and s.ID_stomatologa = w.ID_stomatologa";
        SqlStr = SqlCol +" and UPPER(p.nazwisko) like UPPER('"+Nazwisko+"%') order by ID_wizyty";

        List<Object[]> rows = session.createSQLQuery(SqlStr).list();
        
      
        DefaultTableModel tblModel  = (DefaultTableModel)jTable1.getModel();  
        int rowCount = tblModel.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            tblModel.removeRow(i);
        }
        

        String data[] = new String[7];
        for(Object[] row : rows){
            data[0]=row[0].toString();
            DateFormat dateFormat = new SimpleDateFormat("dd.MM.yy");  
            if( row[1] != null )
                data[1]= dateFormat.format((Date)row[1]); 
            else
                data[1]="";
            data[2]=row[2].toString()+" "+row[3].toString();
            data[3]=row[4].toString()+" "+row[5].toString();

            data[4]=row[6].toString();
            data[5]=row[7].toString();
            data[6]=row[8].toString();
            tblModel.addRow(data);
        }
        
        if ( PozycjaDoUstawienia >= 0 )
        {
            jTable1.setRowSelectionInterval(PozycjaDoUstawienia, PozycjaDoUstawienia);
            PozycjaDoUstawienia = -1;
        }
        appses.CloseSession();
    
        return 0;
    }
