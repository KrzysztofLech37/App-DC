    public void UstawPacjenta( Pacjent Pat)
    {
        jIDField.setText(Integer.toString(Pat.getIdPacjenta()));
        jImieField.setText(Pat.getImie());
        jNazwiskoField.setText(Pat.getNazwisko());
        jPeselField.setText(Pat.getPesel());
        if( Pat.getDataUr() != null)
        {   DateFormat dateFormat = new SimpleDateFormat("dd.MM.yyyy");  
            String strDate = dateFormat.format(Pat.getDataUr());  
            jDataUrField.setText(strDate);
        }
        jMiejscowoscField.setText(Pat.getMiejscowosc());
        jAdresField.setText(Pat.getAdres());
        jKodPocztowyField.setText(Pat.getKodPocztowy());
        jTelField.setText(Integer.toString(Pat.getTelefon()));
    }
