    private void jZapiszButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jZapiszButtonActionPerformed
        Pacjent pat = new Pacjent();
 
        String IDStr =jIDField.getText();
        int Id = 0;
        
        if( !IDStr.isEmpty())
        {
            Id = Integer.parseInt(IDStr);
            pat.setIDPacjenta(Id);
        }
        
        
        pat.setImie(jImieField.getText());
        pat.setNazwisko(jNazwiskoField.getText());
        
        String StrDataUr= jDataUrField.getText();
        if( ! StrDataUr.isEmpty()) {         
            Date DataUr;
         try {
             DataUr = new SimpleDateFormat("dd.MM.yyyy").parse(jDataUrField.getText());
              pat.setDataUr(DataUr);
           } catch (ParseException ex) {
               Logger.getLogger(OkienkoPacjenta.class.getName()).log(Level.SEVERE, null, ex);
           }
        }

        pat.setPesel(jPeselField.getText());
        pat.setAdres(jAdresField.getText());
        pat.setMiejscowosc(jMiejscowoscField.getText());
        pat.setKodPocztowy(jKodPocztowyField.getText());
        String TelStr = jTelField.getText();
        if( ! TelStr.isEmpty()) {
          int Tel=Integer.parseInt(TelStr);       
          pat.setTelefon(Tel);
        }
        
        if( pat.sprawdzDane()) {      
            AppSession appses = new AppSession();
            Session session= appses.OpenSession();      
        
            Transaction trans = session.beginTransaction();     
            if ( Id==0)
                session.save(pat);  
            else
                session.update(pat);
            trans.commit();   
        
            appses.CloseSession();

            setVisible(false);
            
            ListaPat.wczytaj();
        }
        
    }//GEN-LAST:event_jZapiszButtonActionPerformed

