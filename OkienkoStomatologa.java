    private void ZapiszButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ZapiszButtonActionPerformed
                
        Stomatolog stom = new Stomatolog();
               
        String IDStr =jIDField.getText();
        int Id = 0;
        
        if( !IDStr.isEmpty())
        {
            Id = Integer.parseInt(IDStr);
            stom.setIDStomatologa(Id);
        }
               
        stom.setImieStomatologa(jImieField.getText());
        stom.setNazwiskoStomatologa(jNazwiskoField.getText());
        stom.setAktywny(jAktywny.isSelected());
        
        String TelStr = jTelField.getText();
        if( ! TelStr.isEmpty()) {
            int Tel=Integer.parseInt(TelStr);
            stom.setTelefonStomatologa(Tel);
        }     
        
        if( stom.sprawdzDane()) {
        
            AppSession appses = new AppSession();
            Session session= appses.OpenSession();
        
        
            Transaction trans = session.beginTransaction();     
            if ( Id==0)
                session.save(stom);  
            else
                session.update(stom);
            trans.commit();   
        
            appses.CloseSession();

            setVisible(false);
            ListaStom.wczytaj();
        }       
    }//GEN-LAST:event_ZapiszButtonActionPerformed

