    public int wczytaj()
    {
        AppSession appses = new AppSession();
        Session session= appses.OpenSession();
        
        String SqlStr;
        String Nazwisko = jNazwiskoField.getText();
        
        SqlStr = "select * from Uzytkownik where UPPER(nazwisko) like UPPER('"+Nazwisko+"%') order by ID_uzytkownika";

        ArrayList<Uzytkownik> rows = (ArrayList<Uzytkownik>) session.createSQLQuery(SqlStr).addEntity(Uzytkownik.class).list();
        
      
        DefaultTableModel tblModel  = (DefaultTableModel)jTable1.getModel();  
        int rowCount = tblModel.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            tblModel.removeRow(i);
        }
        

        String data[] = new String[4];
        for( Uzytkownik uzyt : rows){
            data[0]= Integer.toString(uzyt.getIdUzytkownika());
            data[1]=uzyt.getImieUzytkownika();
            data[2]=uzyt.getNazwiskoUzytkownika();
            data[3]=uzyt.getLoginUzytkownika();
            tblModel.addRow(data);
        }
        
        if ( PozycjaDoUstawienia >= 0 )
        {
            jTable1.setRowSelectionInterval(PozycjaDoUstawienia, PozycjaDoUstawienia);
            PozycjaDoUstawienia = -1;
        }
        appses.CloseSession();
        return 0;
    }
