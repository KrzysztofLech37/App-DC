    private void EdytujButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EdytujButtonActionPerformed
   
        
        int Selected = jTable1.getSelectedRow();
        if(Selected >= 0 )
        {   PozycjaDoUstawienia = Selected;
            String IDSelected = jTable1.getModel().getValueAt(Selected, 0).toString(); 
            AppSession appses = new AppSession();
            Session session= appses.OpenSession();
            
           Uzytkownik Uzyt;

           Uzyt = (Uzytkownik) session.createSQLQuery("select * from Uzytkownik where ID_Uzytkownika="+IDSelected)
                    .addEntity(Uzytkownik.class).uniqueResult();
            
            OkienkoUzytkownika ds = new OkienkoUzytkownika();
            ds.UstawUzytkownika(Uzyt);
            ds.UstawListe(this);
            
            appses.CloseSession();
            ds.setVisible(true);
            
       
                    

        }
        else
            JOptionPane.showConfirmDialog(null, "Wybierz uzytkownika", "Wskazówka", JOptionPane.DEFAULT_OPTION);
     
 
    }//GEN-LAST:event_EdytujButtonActionPerformed

